function rm(arr, remove = 1) {

    return  [...arr].reverse().slice(remove ).reverse();
}

console.log(rm([1, 2, 3]));
console.log(rm([1, 2, 3], 2));
console.log(rm([1, 2, 3], 5));
console.log(rm([1, 2, 3], 0));

